#import "Point2D.h"

@implementation Point2D

@synthesize x = _x;
@synthesize y = _y;

-(id)init
{
    self = [super init];
    
    if (self) {
        self.x = 0.0;
        self.y = 0.0;
    }
    
    return self;
}

-(id)initWith:(double)x y:(double)y
{
    self = [super init];
    
    if (self) {
        self.x = x;
        self.y = y;
    }
    
    return self;
}

@end
